print("안녕하세요"[0:2]) #index 0부터 2까지(2번째 글자는 포함하지 않음)
print("안녕하세요"[1:3]) #index 1부터 3까지(3번째 글자는 포함하지 않음)
print("안녕하세요"[2:4]) #index 2부터 4까지(4번째 글자는 포함하지 않음)
print("방가입니다"[1:]) #: -> 1부터 끝까지
print("방가입니다"[:3]) #: -> 0부터 3까지(3번째 글자는 포함하지 않음)
print()

