# 정수형(int)과 실수형(float)#################################################
print(273)
print(type(0))
print(27.3)
print(type(27.3))
print()

# 사칙연산(+ - * /)###########################################################
print("1+2 = ", 1+2)
print("1-2 = ", 1-2)
print("1*2 = ", 1*2)
print("1/2 = ", 1/2)
print()

# ++정수 나누기 연산자(//)#####################################################
print("3/2 = ", 3/2)
print("3//2 = ", 3//2) #소수점 이하의 자릿수는 떼고, 정수 부분만 남음
print()

# ++나머지 연산자(%)###########################################################
print("5 % 2 = ", 5%2)

# ++제곱 연산자(**)############################################################
print("2**1 = ", 2**1)
print("2**2 = ", 2**2)
print("2**3 = ", 2**3)
print("2**4 = ", 2**4)
print()

# ++복합 대입 연산자(+= -= *= /= %= **=)#######################################
number = 100
number += 10 #=110
number += 20 #=130
number += 30 #=160
print("number: ", number) #160
print()

